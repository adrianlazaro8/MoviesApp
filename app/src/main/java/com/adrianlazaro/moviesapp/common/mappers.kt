package com.adrianlazaro.moviesapp.common

import com.adrianlazaro.domain.Movie
import com.adrianlazaro.moviesapp.data.database.MovieRoom
import com.adrianlazaro.moviesapp.data.network.MovieServer

fun Movie.toRoomMovie(): MovieRoom =
    MovieRoom(
        id,
        title,
        overview,
        releaseDate,
        posterPath,
        backdropPath,
        originalLanguage,
        originalTitle,
        popularity,
        voteAverage,
        favorite
    )

fun MovieRoom.toDomainMovie(): Movie = Movie(
    id,
    title,
    overview,
    releaseDate,
    posterPath,
    backdropPath,
    originalLanguage,
    originalTitle,
    popularity,
    voteAverage,
    favorite
)

fun MovieServer.toDomainMovie(): Movie =
    Movie(
        0,
        title,
        overview,
        releaseDate,
        posterPath,
        backdropPath ?: posterPath,
        originalLanguage,
        originalTitle,
        popularity,
        voteAverage,
        false
    )